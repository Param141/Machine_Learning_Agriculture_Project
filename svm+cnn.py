from tensorflow.keras.preprocessing.image import ImageDataGenerator

img_height, img_width = 224, 224
batch_size = 32

train_datagen = ImageDataGenerator(
    rescale=1./255,
    validation_split=0.2
)

train_generator = train_datagen.flow_from_directory(
    base_dir,
    target_size=(img_height, img_width),
    batch_size=batch_size,
    class_mode='categorical',
    subset='training',
    shuffle=False
)

val_generator = train_datagen.flow_from_directory(
    base_dir,
    target_size=(img_height, img_width),
    batch_size=batch_size,
    class_mode='categorical',
    subset='validation',
    shuffle=False
)

class_names = list(train_generator.class_indices.keys())
print("Classes:", class_names)

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Conv2D, MaxPooling2D, GlobalAveragePooling2D, Input

cnn = Sequential([
    Input(shape=(224, 224, 3)),
    Conv2D(32, (3, 3), activation='relu'),
    MaxPooling2D(2, 2),
    Conv2D(64, (3, 3), activation='relu'),
    MaxPooling2D(2, 2),
    Conv2D(128, (3, 3), activation='relu'),
    MaxPooling2D(2, 2),
    GlobalAveragePooling2D(),  # replaces Flatten()
])

cnn.summary()

train_features = cnn.predict(train_generator, verbose=1)
val_features = cnn.predict(val_generator, verbose=1)

train_labels = train_generator.classes
val_labels = val_generator.classes

print("Train features shape:", train_features.shape)
print("Train labels shape:", train_labels.shape)

# Step 6: Train SVM Classifier
from sklearn.svm import SVC
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import accuracy_score, classification_report, confusion_matrix
import numpy as np

# Scale features for SVM (important)
scaler = StandardScaler()
train_features_scaled = scaler.fit_transform(train_features)
val_features_scaled = scaler.transform(val_features)

# Train SVM with RBF kernel (you can tune params later)
svm_clf = SVC(kernel='rbf', C=1, gamma='scale', random_state=42)
svm_clf.fit(train_features_scaled, train_labels)

from sklearn.metrics import accuracy_score, classification_report, confusion_matrix
import matplotlib.pyplot as plt
import seaborn as sns

# Predict on validation set
val_preds = svm_clf.predict(val_features_scaled)

# Calculate accuracy
val_acc = accuracy_score(val_labels, val_preds)
print(f"Validation Accuracy (SVM on CNN features): {val_acc*100:.2f}%")

# Detailed classification report
print("\nClassification Report:\n", classification_report(val_labels, val_preds, target_names=class_names))

# Confusion matrix plot
cm = confusion_matrix(val_labels, val_preds)
